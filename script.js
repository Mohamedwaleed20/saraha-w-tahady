let usedTruth = [];
let usedDare = [];
let usedAll = [];
let counter = 0;

function playSound() {
  document.getElementById("sound").play();
}

function getRandomCard(arr, usedArr) {
  if (usedArr.length === arr.length) usedArr.length = 0; // إعادة التهيئة بعد انتهاء الكروت
  let card;
  do {
    card = arr[Math.floor(Math.random() * arr.length)];
  } while (usedArr.includes(card));
  usedArr.push(card);
  return card;
}

function showCard(type) {
  let cardText = "";
  if (type === "truth") {
    cardText = getRandomCard(truthCards, usedTruth);
  } else if (type === "dare") {
    cardText = getRandomCard(dareCards, usedDare);
  } else {
    const all = [...truthCards, ...dareCards];
    cardText = getRandomCard(all, usedAll);
  }

  document.getElementById("cardText").textContent = cardText;
  counter++;
  document.getElementById("counter").textContent = `عدد الكروت: ${counter}`;
  playSound();
}

function toggleTheme() {
  document.getElementById("body").classList.toggle("light-mode");
}
